package org.opendaylight.iotdm.constant.enumeration;

import java.math.BigInteger;

/**
 * Created by wenxshi on 3/31/15.
 */
public enum StatModelType  implements EnumApi {
    EVENTBASED("EVENTBASED", BigInteger.valueOf(1));


    private String interpretation;
    private BigInteger value;

    StatModelType(String interpretation, BigInteger value) {
        this.interpretation = interpretation;
        this.value = value;
    }

    public String getInterpretation() {
        return interpretation;
    }

    public BigInteger getValue() {
        return value;
    }
}
